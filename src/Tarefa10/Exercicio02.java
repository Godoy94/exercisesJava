package Tarefa10;

public class Exercicio02 {

    public static void main(String[] Args) {
        // 2) Apresentar os resultados de uma tabuada de multiplicar (de 1 até 10) de um
        // número qualquer.
        for (int i = 0; i <= 10; i++) {
            for (int j = 0; j <= 10; j++) {
                System.out.println(i + " x " + j + " = " + i * j);
            }
        }
    }
}
